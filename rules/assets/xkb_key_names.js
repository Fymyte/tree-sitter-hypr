function make_trie(words) {
  trie = {}

  function __insert(key, node) {
    const [char, remainder] = [key.charAt(0), key.substring(1)];
    is_end = remainder.length == 0

    if (!node[char])
      node[char] = { end: is_end, next: {}, passed: 1 }
    else
      node[char].passed += 1

    if (!is_end)
      __insert(remainder, node[char].next)
    else
      node[char].end = true
  }

  words.forEach((word) => {
    __insert(word, trie)
  })
  return trie
}

module.exports = {
  symbol: (_$) => {
    trie = make_trie(KEYS)

    function __make_choice_inner(letter, desc, immediate) {
      immediate = immediate ? token.immediate : (t) => t
      if (Object.entries(desc.next).length)
        return seq(immediate(letter), desc.end ? optional(__make_choice(desc.next)) : __make_choice(desc.next))
      else return immediate(letter)
    }

    function __make_choice(trie) {
      entries = Object.entries(trie)
      if (entries.length == 1) return __make_choice_inner(...entries[0], true)
      return choice(
        ...entries.map(([letter, desc]) => __make_choice_inner(letter, desc, true))
      )
    }

    return choice(
      ...Object.entries(trie).map(([letter, desc]) => __make_choice_inner(letter, desc, false))
    )
  },
};
